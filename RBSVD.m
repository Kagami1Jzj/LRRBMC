function [U,A,V] = RBSVD(X)
X0 = X.w;
Xi = X.x;
Xj = X.y;
Xk = X.z;
q1 = complex((X0+Xj),(Xi+Xk));
q2 = complex((X0-Xj),(Xi-Xk));
[U1,A1,V1] = svd(q1);
[U2,A2,V2] = svd(q2);
[m1,n1] = size(U1);
[m2,n2] = size(A1);
[m3,n3] = size(V1);
U = zeros(2*m1,2*n1);
U(1:m1,1:n1) = U1;
U(m1+1:2*m1,n1+1:2*n1) = U2;
A = zeros(2*m2,2*n2);
A(1:m2,1:n2) = A1;
A(m2+1:2*m2,n2+1:2*n2) = A2;
V = zeros(2*m3,2*n3);
V(1:m3,1:n3) = V1;
V(m3+1:2*m3,n3+1:2*n3) = V2;